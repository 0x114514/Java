/* 
    float   单精度
    double  双精度

    float会使用一部分表示数，一部分表示幂
    
    声明float变量需要用f或F结尾
*/
public class Variable_Float_Double {
    public static void main(String[] args){
        float   variableFloat = 112.4f;
        System.out.println(1 + variableFloat);
        double  variableDouble = 12.3;
        System.out.println(1 + variableDouble);
    }
}
